package privateinheritanceoop50;


public class Person {
   private String name;
   private String gender;
   
   public String getname(){
       return name;
   }
   public void setname(String name){
       this.name=name;
       
   }
   public String getgender(){
       return gender;
   }
   public void setgender(String gender){
       this.gender=gender;
   }
}
