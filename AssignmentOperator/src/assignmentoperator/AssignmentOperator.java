
package assignmentoperator;


public class AssignmentOperator {

    
    public static void main(String[] args) {
       int a=5,b=2;
       a+=b;
       System.out.println("A ="+a);
       a-=b;
       System.out.println("A ="+a);
       a*=b;
       System.out.println("A ="+a);
       a/=b;
       System.out.println("A ="+a);
       a%=b;
       System.out.println("A ="+a);
    }
    
}
