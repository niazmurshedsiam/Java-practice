
package datatype;


public class Datatype {

    
    public static void main(String[] args) {
        boolean b;
        short s;
        int i;
        char c;
        float f = 3.14f;
        double d = 3.14162;
        b = true;
        c = 'a';
        i = 10;
        
        System.out.println("Boolean b="+b);
        System.out.println("c="+c);
        System.out.println("i="+i);
        System.out.println("f="+f);
        System.out.println("d="+d);
      
    }
    
}
