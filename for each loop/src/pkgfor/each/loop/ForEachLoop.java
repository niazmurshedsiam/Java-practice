
package pkgfor.each.loop;


public class ForEachLoop {

   
    public static void main(String[] args) {
       String [] name = new String[4];
       name [0] = "siam";
        name [1] = "niaz";
         name [2] = "murshed";
          name [3] = "sadnan";
        for (int i = 0; i < 4; i++) {
            System.out.println(name[i]);
        }
    }
    
    
}
